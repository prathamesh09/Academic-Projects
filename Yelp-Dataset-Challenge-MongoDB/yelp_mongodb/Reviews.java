    public Reviews(String obj) {
        initComponents();
        String[] columnNames = {"Date", "Business_ID", "Text", "USER_ID"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);

        if(obj!=null){
            if(obj.length()>0){
                MongoClient mongoClient = new MongoClient();//"localhost",27017);
                DB db = mongoClient.getDB("yelp");
                BasicDBObject select = new BasicDBObject();
                select.put("_id",0);
                select.put("date",1);
                select.put("business_id", 1);
                select.put("text", 1); 
                select.put("user_id", 1);
                DBCollection review = db.getCollection("review");
                DBCursor locCursor = review.find(new BasicDBObject(), select);
                
                int row = locCursor.count();
                
                
                
                
                while(locCursor.hasNext()){
                    DBObject dbo = locCursor.next();
                    String bid = dbo.get("business_id").toString();
                    if(bid.equals(obj)){
                        String dte = dbo.get("date").toString();
                        String txt = dbo.get("text").toString();
                        String uid = dbo.get("user_id").toString();
                    
                        model.addRow(new Object[]{dte, bid, txt, uid});
                    }
                }
                jTable1.setModel(model);
                jTable1.repaint();
           }
        }
    }
